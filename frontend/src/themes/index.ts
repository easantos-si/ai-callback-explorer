export function getThemeById(id: string): Theme {
  return THEMES.find((t) => t.id === id) || THEMES[0];
}

export function applyTheme(theme: Theme): void {
  const root = document.documentElement;
  const c = theme.colors;
  root.style.setProperty('--bg-primary', c.bgPrimary);
  root.style.setProperty('--bg-secondary', c.bgSecondary);
  root.style.setProperty('--bg-tertiary', c.bgTertiary);
  root.style.setProperty('--bg-hover', c.bgHover);
  root.style.setProperty('--bg-active', c.bgActive);
  root.style.setProperty('--border-color', c.borderColor);
  root.style.setProperty('--border-light', c.borderLight);
  root.style.setProperty('--text-primary', c.textPrimary);
  root.style.setProperty('--text-secondary', c.textSecondary);
  root.style.setProperty('--text-muted', c.textMuted);
  root.style.setProperty('--accent', c.accent);
  root.style.setProperty('--accent-hover', c.accentHover);
  root.style.setProperty('--accent-glow', c.accentGlow);
  root.style.setProperty('--success', c.success);
  root.style.setProperty('--warning', c.warning);
  root.style.setProperty('--danger', c.danger);
  root.style.setProperty('--info', c.info);
  root.style.setProperty('--json-key', c.jsonKey);
  root.style.setProperty('--json-string', c.jsonString);
  root.style.setProperty('--json-number', c.jsonNumber);
  root.style.setProperty('--json-boolean', c.jsonBoolean);
  root.style.setProperty('--json-null', c.jsonNull);
  root.dataset.themeMode = theme.mode;
  root.dataset.themeId = theme.id;
}
